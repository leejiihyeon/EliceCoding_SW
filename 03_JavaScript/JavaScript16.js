/* 숫자 20을 데이터로 갖는 변수 num을 선언합니다. */
var num  = 20;

/* ++num 값을 출력해 봅니다. */
document.write(++num);

/* --num 값을 출력해 봅니다. */
document.write(--num);

/* num++과 num값을 출력해 봅니다. */
document.write(num++);
document.write(num);

/* num--과 num값을 출력해 봅니다. */
document.write(num--);
document.write(num);